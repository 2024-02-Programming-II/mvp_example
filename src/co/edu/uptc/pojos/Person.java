package co.edu.uptc.pojos;

public class Person {
    private String name;
    private String Lastname;


    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getLastname() {
        return Lastname;
    }
    public void setLastname(String lastname) {
        Lastname = lastname;
    }
    
    
}
